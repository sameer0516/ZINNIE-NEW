export function buildProductSchema(slug, apiProduct) {
    const seo = productSeoData[slug];
    if (!seo || !apiProduct) return null;

    const imageUrl = apiProduct.image?.startsWith("http")
        ? apiProduct.image
        : `https://api.zinniezeera.com/${apiProduct.image
            ?.replace(/\\/g, "/")
            .replace(/^\/+/, "")}`;

    return {
        "@context": "https://schema.org",
        "@type": "Product",
        name: apiProduct.title || seo.name,
        image: [imageUrl],
        description: apiProduct.metaDescription || apiProduct.description || seo.metaDescription || "",
        brand: { "@type": "Brand", name: "Zinnie Zeera" },
        sku: seo.sku,
        category: seo.category,
        url: seo.canonicalUrl,
        aggregateRating: {
            "@type": "AggregateRating",
            ratingValue: seo.ratingValue,
            reviewCount: seo.reviewCount,
        },
        offers: {
            "@type": "Offer",
            priceCurrency: "INR",
            price: seo.price,
            availability: "https://schema.org/InStock",
            itemCondition: "https://schema.org/NewCondition",
            url: seo.canonicalUrl,
        },
    };
}

