export function buildFaqSchema(slug) {
    const seo = productSeoData[slug];
    if (!seo?.faq?.length) return null;

    return {
        "@context": "https://schema.org",
        "@type": "FAQPage",
        mainEntity: seo.faq.map((item) => ({
            "@type": "Question",
            name: item.question,
            acceptedAnswer: { "@type": "Answer", text: item.answer },
        })),
    };
}

